#include "../libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;

	i = 0;
	c %= 256;
	while (i < n && *(unsigned char *)(s + i) != (unsigned char)c)
		i++;
	if (i == n)
		return (NULL);
	return ((void *)(s + i));
}

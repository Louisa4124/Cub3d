#include "../libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	i;
	size_t	n;

	i = 0;
	n = ft_strlen(needle);
	if (n == 0)
		return ((char *) haystack);
	while (haystack[i] && i + n <= len)
	{
		if (!ft_strncmp(&haystack[i], needle, n))
			return ((char *) &haystack[i]);
		i++;
	}
	return (NULL);
}
